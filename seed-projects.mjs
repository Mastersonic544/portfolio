async function seed() {
  console.log('Checking existing projects…');
  const snap = await getDocs(collection(db, 'projects'));
  const existingSlugs = new Set(snap.docs.map(d => d.data().slug));
  console.log(`Found ${existingSlugs.size} existing projects.`);

  const toSeed = ALL_PROJECTS.filter(p => !existingSlugs.has(p.slug));
  const skipped = ALL_PROJECTS.length - toSeed.length;

  if (!toSeed.length) {
    console.log('Nothing to seed — all 29 projects already exist.');
    process.exit(0);
  }

  console.log(`Seeding ${toSeed.length} projects (skipping ${skipped} already existing)…`);

  // Firestore batch limit is 500 ops — split if needed
  const BATCH_SIZE = 400;
  for (let i = 0; i < toSeed.length; i += BATCH_SIZE) {
    const chunk = toSeed.slice(i, i + BATCH_SIZE);
    const batch = writeBatch(db);
    for (const project of chunk) {
      const ref = doc(collection(db, 'projects'));
      batch.set(ref, {
        ...project,
        imageCount: 0,
        published:  true,
        createdAt:  serverTimestamp(),
        updatedAt:  serverTimestamp(),
      });
    }
    await batch.commit();
    console.log(`  Batch committed: ${chunk.length} projects.`);
  }

  console.log(`Done! ${toSeed.length} projects seeded successfully.`);
  process.exit(0);
}

seed().catch(err => {
  console.error('Seed failed:', err.message ?? err);
  process.exit(1);
});
